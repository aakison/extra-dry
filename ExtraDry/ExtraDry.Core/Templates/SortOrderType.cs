namespace ExtraDry.Core;

/// <summary>
/// The sort order type, either ascending or descending
/// </summary>
public enum SortOrderType {

    /// <summary>
    /// Sort is performend ascending, e.g. 1, 2, 3... or a, b, c...
    /// </summary>
    Ascending,

    /// <summary>
    /// Sort is performend descending, e.g. 10, 9, 8... or z, y, x...
    /// </summary>
    Descending
}
